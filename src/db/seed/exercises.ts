async function insertInChunks(data: ExerciseSeed[], chunkSize: number) {
  for (let i = 0; i < data.length; i += chunkSize) {
    const chunk = data.slice(i, i + chunkSize);
    await db.insert(exercises).values(chunk);
  }
}

export async function seedExercises() {
  const result = await db
    .select({ count: sql<number>`count(*)` })
    .from(exercises);

  const count = result[0]?.count ?? 0;

  if (count > 0) {
    console.log(
      `Exercises already seeded (${count} found). Skipping.`
    );
    return;
  }

  console.log(`Seeding ${allExercises.length} exercises...`);
  await insertInChunks(allExercises, 50);
  console.log('Exercise seed complete.');
}
